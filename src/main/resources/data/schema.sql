DROP SCHEMA IF EXISTS "HSQLDB_EBEDDED";
CREATE SCHEMA "HSQLDB_EBEDDED";
CREATE TEXT TABLE IF NOT EXISTS "HSQLDB_EBEDDED.COSTUMER" (ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,Title VARCHAR(16), Name VARCHAR(32), Street VARCHAR(32), City VARCHAR(32), ZIP VARCHAR(8));
CREATE TEXT TABLE IF NOT EXISTS "HSQLDB_EBEDDED.ORDER" (ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, ID_Kunde INTEGER, Order_Date DATE, Nr INTEGER, Amount FLOAT);
CREATE TEXT TABLE IF NOT EXISTS "HSQLDB_EBEDDED.ORDER_NOTE" (ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, Note_Text VARCHAR(2048));
CREATE INDEX "ORDER.ORER_DATE" ON "HSQLDB_EBEDDED.AUFTRAG"(Datum);
SET WRITE_DELAY 0 MILLIS;
SET TABLE "HSQLDB_EBEDDED.COSTUMER" SOURCE 'costumer.csv;ignore_first=true;fs=\t;encoding=UTF-8;cache_rows=5000;cache_size=1000';
SET TABLE "HSQLDB_EBEDDED.ORDER" SOURCE 'order.csv;ignore_first=true;fs=\t;encoding=UTF-8;cache_rows=20000;cache_size=4000';
SET TABLE "HSQLDB_EBEDDED.ORDER_NOTE" SOURCE 'order_note.csv;ignore_first=true;fs=\t;encoding=UTF-8';
